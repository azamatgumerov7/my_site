// 1. Берем нужный инструмент из библиотеки 'pg'
// 'Pool' — это пул соединений (несколько каналов к базе данных)
const { Pool } = require('pg');

// 2. Создаем настройки подключения
// Это как адрес, логин и пароль от твоей базы данных
const pool = new Pool({
  user: 'postgres',       // ← Имя пользователя (по умолчанию 'postgres')
  password: '6767',     // ← Пароль, который ты указал при установке PostgreSQL
  host: 'localhost',      // ← База на твоем компьютере
  port: 5432,             // ← Порт по умолчанию для PostgreSQL
  database: 'mywebsite'   // ← Название базы данных (создадим её позже)
});

// 3. Создаем функцию для выполнения SQL-запросов
// Это будет наша универсальная функция для общения с БД
async function query(text, params) {
  // pool.query() — отправляет запрос в базу данных
  const result = await pool.query(text, params);
  // Возвращаем только строки результата (без служебной информации)
  return result.rows;
}

// 4. Экспортируем функцию, чтобы использовать в других файлах
// Как сказать: "Эй, server.js, вот тебе функция query — пользуйся!"
module.exports = { query };